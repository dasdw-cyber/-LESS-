# 🌟 新增辅助函数：根据选中的基准日期，计算并返回前后各一天的 3 天列表
def get_three_days(base_date_str):
    base_date = pd.to_datetime(base_date_str)
    prev_date = (base_date - pd.Timedelta(days=1)).strftime('%Y-%m-%d')
    next_date = (base_date + pd.Timedelta(days=1)).strftime('%Y-%m-%d')
    return [prev_date, base_date_str, next_date]


