def get_spectral_diffuse_string(f_par, wl_list, coeffs_dict):
    diffuse_ratios = []
    available_wls = list(coeffs_dict.keys())
    for wl in wl_list:
        closest_wl = min(available_wls, key=lambda x: abs(x - wl))
        poly_coeffs = coeffs_dict[closest_wl]
        ratio = np.polyval(poly_coeffs, f_par)

        if np.isnan(ratio): ratio = 0.0
        ratio = max(0.0, min(1.0, ratio))
        diffuse_ratios.append(f"{ratio:.4f}")

    return ",".join(diffuse_ratios)


